<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">Our Mission</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">StagePhoto.ru is a community of photographers, musicians, theater enthusiasts, and live music lovers. Our goal is to create an inspiring, respectful, and safe space for sharing creative work.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">1. Be Respectful</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">Treat other users with respect. Constructive criticism is welcome, but insults, harassment, or aggressive behavior are not allowed. Remember that behind every account is a real person.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">2. Content Quality</h2>
    <ul class="list-disc list-inside text-gray-600 dark:text-gray-400 space-y-1 ml-4">
        <li>Upload only your own photographs</li>
        <li>Give proper attribution when posting others' work (with permission)</li>
        <li>Avoid excessive editing or misleading images</li>
        <li>Post photos relevant to concert and theater photography</li>
        <li>Don't upload identical or repetitive images</li>
    </ul>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">3. Comments and Feedback</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">Leave meaningful comments. If you don't like a work, explain why, but do it constructively. Rate photos honestly and objectively. Don't leave spam or self-promotion in others' comments.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">4. Commercial Use</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">Use the request system to obtain commercial licenses. Don't copy or use others' work without permission. Respect other photographers' copyrights.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">5. Help the Community</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">Participate in discussions, help new photographers, share your experience. A strong community is built on mutual support. Answer questions and maintain constructive dialogue.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">6. Reports and Moderation</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">If you see content that violates the rules, use the report button. Moderators will review all reports within 24 hours. Don't abuse the report function—false reports may lead to restrictions.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">7. Consequences of Violations</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">Rule violations may result in:</p>
    <ul class="list-disc list-inside text-gray-600 dark:text-gray-400 space-y-1 ml-4">
        <li>Warning</li>
        <li>Temporary suspension (1 day to 1 month)</li>
        <li>Permanent account deletion</li>
        <li>Ban on creating new accounts</li>
    </ul>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed mt-2">The severity of the penalty depends on the violation's gravity and previous violation history.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">8. Copyright and Attribution</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">Always credit the author when posting others' work (with permission). When using your work on other platforms, we appreciate a link back to StagePhoto.ru as the source.</p>
</section>

<section class="mb-8">
    <h2 class="text-xl font-semibold text-gray-900 dark:text-white mb-3">9. Privacy</h2>
    <p class="text-gray-600 dark:text-gray-400 leading-relaxed">Don't post personal information about others without their consent. Respect community members' privacy.</p>
</section>

